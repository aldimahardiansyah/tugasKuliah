// Waktu
const myDate = new Date();
console.log(myDate); // Tue Apr 20 2021 05:17:51 GMT+0700 (Western Indonesia Time)

const month = myDate.getMonth();
console.log(month); // Nilai kembaliannya adalah bulan dalam bentuk angka (0 sampai 11), 0 berarti Januari.

const day = myDate.getDay();
console.log(day); // Nilai kembaliannya adalah hari dalam seminggu dari 0 sampai 6. 0 berarti minggu

